<h3>Badref</h3>
Блокирование переходов с некоторых сайтов<br><br>

Некоторые сайты портят поведенческую статистику спам-переходами. Эти переходы можно заблокировать применив этот солюшн.

Включить его можно в конфиге:
<?solutions\highlighter::out('"badrefSolution" => ["enabled" => true ],')?>
