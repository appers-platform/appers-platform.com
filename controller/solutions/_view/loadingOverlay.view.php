<h3>loadingOverlay</h3>
Отображает полупрозрачный серый слой поверх контента страницы с иконкой загрузки в центре при клике на элемент,
для которого установлен css класс "loadingOverlay".

<br><br>

Включить его можно в конфиге:
<?solutions\highlighter::out('"loadingOverlaySolution" => ["enabled" => true ],')?>
