Работа с файловой системой.<br>
<br>
<b>Методы</b>:
<ul>
	<li>
		<i>\solutions\fs::copy($target, $destination, $content_only = false)</i>
		- Копирует $target в $destination. <br>
		$target - что <br>
		$destination - куда <br>
		$content_only - если указан этот параметр и $target - это директория, то будет скопирован только контент указанной директории. <br>
	</li>
</ul>
