<h3>FBJsSDK</h3>
Загрузчик для Facebook JavaScript SDK<br><br>

Включить его и указать Facebook Application ID можно в конфиге:
<?solutions\highlighter::out('"FBJsSDKSolution" => ["enabled" => true, "app_id" => <your-16-digit-app-id> ],')?>
Указывать app_id не обязательно, если Вы используете функции не требующие его.
